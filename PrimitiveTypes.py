# python contains numbers and floats as opposed to javascript where every number is a number

add = 2 + 2
print(add)

subtract = 50 - 10
print(subtract)

# division will always return a floating point / use // to floor the number returned from division
divide = 10 / 4
print(divide)

floorDivide = 10 // 4
print(floorDivide)

remainder = 3 % 2
print(remainder)